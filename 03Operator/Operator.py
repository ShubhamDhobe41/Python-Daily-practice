# Arithmatic Operator
num1 = 10
num2 = 20
sum = num1 + num2
print("Sum:", sum)

# Assignment Operator
num1 = 10
num2 += 5  # num2 = num2 + 5
print("Updated num2 with + :", num2)
num2 -= 5
print("Updated num2 with - :", num2)


# Comparison Operator
print("Is num1 equal to num2? : ", num1 == num2)
print("Is num1 not equal to num2? : ", num1 != num2)
print("Is num1 greater than num2? : ", num1 > num2)
print("Is num1 less than num2? : ", num1 < num2)
print("Is num1 greater than or equal to num2? : ", num1 >= num2)
print("Is num1 less than or equal to num2? : ", num1 <= num2)


# Logical Operator
print("Is num1 greater than 5 and num2 less than 30? : ", num1 > 5 and num2 < 30)
print("Is num1 less than 5 or num2 greater than 15? : ", num1 < 5 or num2 > 15)

